
-- New Set of Tables
-- LOGIN CREDENTIALS
CREATE TABLE IF NOT EXISTS LoginCredentials (
	email VARCHAR (100) PRIMARY KEY,
    password VARCHAR (256)
);

-- Table 1: Customer Information
CREATE TABLE IF NOT EXISTS Customer (
	customer_id INT AUTO_INCREMENT PRIMARY KEY,
    first_name VARCHAR(50),
    last_name VARCHAR(50),
    email VARCHAR(100) UNIQUE,
    phone_number VARCHAR(20),
    delivery_address TEXT,
    FOREIGN KEY (email) REFERENCES LoginCredentials (email)
);

-- Table 2: Supplier Information
CREATE TABLE IF NOT EXISTS Supplier (
	supplier_id INT AUTO_INCREMENT PRIMARY KEY,
    supplier_fname VARCHAR(50),
    supplier_lname VARCHAR(50),
    email VARCHAR(100) UNIQUE,
    phone VARCHAR(20),
    address TEXT,
    supplier_rating DECIMAL(2, 1) CHECK (supplier_rating BETWEEN 0 and 5),
    FOREIGN KEY (email) REFERENCES LoginCredentials (email)
);

-- Table 3: Manufacturer Information
CREATE TABLE IF NOT EXISTS Manufacturer (
	manufacturer_id INT AUTO_INCREMENT PRIMARY KEY,
    manufacturer_name VARCHAR(50),
    address TEXT
);

-- Table 4: Item Entity
CREATE TABLE IF NOT EXISTS Item (
	item_id INT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(100),
    manufacturer_id INT,
    srp DECIMAL (10, 2),
    manu_price DECIMAL (10,2),
    description TEXT,
    FOREIGN KEY (manufacturer_id) REFERENCES Manufacturer (manufacturer_id)
);

-- Table 9: Supplier Inventory
CREATE TABLE IF NOT EXISTS Inventory (
    inventory_entry_id INT AUTO_INCREMENT PRIMARY KEY,
    item_id INT,
    supplier_id INT,
    quantity INT,
    price DECIMAL(10,2),
    FOREIGN KEY (item_id) REFERENCES Item(item_id),
    FOREIGN KEY (supplier_id) REFERENCES Supplier(supplier_id)
);

-- Table 5: Shopping Cart
CREATE TABLE IF NOT EXISTS ShoppingCart (
	shoppingcart_id INT AUTO_INCREMENT PRIMARY KEY,
    customer_id INT,
    inventory_entry_id INT,
    quantity INT,
    FOREIGN KEY (customer_id) REFERENCES Customer (customer_id),
    FOREIGN KEY (inventory_entry_id) REFERENCES Inventory (inventory_entry_id)
);

-- Table 6: Wishlist
CREATE TABLE IF NOT EXISTS Wishlist (
	wishlist_id INT AUTO_INCREMENT PRIMARY KEY,
    customer_id INT,
    inventory_entry_id INT,
    FOREIGN KEY (customer_id) REFERENCES Customer (customer_id),
    FOREIGN KEY (inventory_entry_id) REFERENCES Inventory (inventory_entry_id)
);

-- Table 7: Buyer Order Information
-- receipt 
CREATE TABLE IF NOT EXISTS BuyerOrderInfo (
	buyer_order_information_id INT AUTO_INCREMENT PRIMARY KEY,
    shoppingcart_id INT,
    order_date DATE,
    total_amount DECIMAL (10, 2),
    status ENUM ('Pending', 'Completed', 'Cancelled', 'Returned'),
    FOREIGN KEY (shoppingcart_id) REFERENCES ShoppingCart (shoppingcart_id)
);

-- Table 8: Buyer Order Item
-- has a weak entity relationship with buyer order information
CREATE TABLE IF NOT EXISTS BuyerOrderItem (
	buyer_order_item_id INT AUTO_INCREMENT PRIMARY KEY,
	buyer_order_information_id INT,
    price_at_order DECIMAL (10, 2),
    FOREIGN KEY (buyer_order_information_id) REFERENCES BuyerOrderInfo(buyer_order_information_id)
);

-- Table 10: Supplier Order Information
CREATE TABLE IF NOT EXISTS SupplierOrderInfo (
	supplier_order_id INT AUTO_INCREMENT PRIMARY KEY,
    supplier_id INT,
    order_date DATE,
    manufacturer_id INT,
    total_amount DECIMAL (10, 2),
    status ENUM ('Pending', 'Completed', 'Cancelled', 'Returned'),
    FOREIGN KEY (supplier_id) REFERENCES Supplier (supplier_id),
    FOREIGN KEY (manufacturer_id) REFERENCES Manufacturer (manufacturer_id)
);

-- Table 11: Supplier Order Item
CREATE TABLE IF NOT EXISTS SupplierOrderItem (
	supplier_order_detail_id INT AUTO_INCREMENT PRIMARY KEY,
    supplier_order_id INT,
    item_id INT,
    quantity INT,
    price_at_order DECIMAL (10, 2),
    FOREIGN KEY (supplier_order_id) REFERENCES SupplierOrderInfo (supplier_order_id),
    FOREIGN KEY (item_id) REFERENCES Item(item_id)
);

-- Table 12: Buyer Order Payment
CREATE TABLE IF NOT EXISTS BuyerOrderPayment (
	payment_id INT AUTO_INCREMENT PRIMARY KEY,
    buyer_order_information_id INT,
    payment_date DATE,
    payment_mode ENUM ('Cash on Delivery', 'Online Payment', 'Credit Card', 'Debit Card'),
    payment_status ENUM ('Unpaid', 'Paid', 'Refunded'),
    FOREIGN KEY (buyer_order_information_id) REFERENCES BuyerOrderInfo (buyer_order_information_id)
);

-- Table 13: Supplier Order Payment
CREATE TABLE IF NOT EXISTS SupplierOrderPayment (
    payment_id INT AUTO_INCREMENT PRIMARY KEY,
    supplier_order_id INT,
    payment_date DATE,
    payment_mode ENUM ('Cash on Delivery', 'Online Payment', 'Credit Card', 'Debit Card'),
    payment_status ENUM ('Unpaid', 'Paid', 'Refunded'),
    FOREIGN KEY (supplier_order_id) REFERENCES SupplierOrderInfo (supplier_order_id)
);
    

    



